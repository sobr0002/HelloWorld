package Funktioner.ArithmeticOperatorsInFunctions;
// Write a method that takes three integers as parameter, sums the integers and returns the result.
public class SubtractionNumbers {
    public static double subtractionNumbers(double a, double b, double c) {
        return a - b - c;
    }
    public static void main(String[] args) {
        System.out.println(subtractionNumbers(100, 40, 30));
    }
}
